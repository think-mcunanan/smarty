class AppError extends ErrorHandler {

    /**
     * エラー担当
     * Error Handling
     *
     * @param string $method
     * @param Array $messages
     */
    function __construct($method, $messages) {
        $this->controller = new AppController();
    	$storeid = $messages[0]['url'];
        $this->controller->redirect(MOBASUTE_PATH.$storeid);
        exit();
    }
}
